namespace Rf\Core\Utils\Format;

/**
 * Class Php
 *
 * @package Rf\Core\Utils\Format
 */
abstract class Php {

    /**
     * @var array List of reserved words in PHP
     */
    protected static $reservedKeywords = [
        '__halt_compiler', 'abstract', 'and', 'array', 'as', 'break', 'callable', 'case', 'catch', 'class', 'clone', 'const', 'continue', 'declare', 'default', 'die', 'do', 'echo', 'else', 'elseif', 'empty', 'enddeclare', 'endfor', 'endforeach', 'endif', 'endswitch', 'endwhile', 'eval', 'exit', 'extends', 'final', 'for', 'foreach', 'function', 'global', 'goto', 'if', 'implements', 'include', 'include_once', 'instanceof', 'insteadof', 'interface', 'isset', 'list', 'namespace', 'new', 'or', 'print', 'private', 'protected', 'public', 'require', 'require_once', 'return', 'static', 'switch', 'throw', 'trait', 'try', 'unset', 'use', 'var', 'while', 'xor'
    ];

    /**
     * Check if a keyword is reserved in the PHP language
     *
     * @param string $keyword
     *
     * @return bool
     */
    public static function isReservedKeyword($keyword) {

        return in_array(strtolower($keyword), self::$reservedKeywords);

    }

}